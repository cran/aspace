"cos_d" <-
function(theta=0) {

  #=======================================================
  #
  #  TITLE:     COMPUTE COSINE IN DEGREES
  #  FUNCTION:  cos_d()
  #  AUTHOR:    TARMO K. REMMEL 
  #  DATE:      16 JANUARY 2006
  #  CALLS:     NA
  #  NEEDS:     
  #  NOTES:     TO SIMPLIFY THE USE OF RADIANS IN R
  #
  #=======================================================

  return(cos(theta*pi/180))
}

